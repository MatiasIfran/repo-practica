package ejercicio1;

public class Gastos {

	private Integer id_comprobante;
	private String descripcion;
	private Double total_Gasto;
	
	public Integer getId_comprobante() {
		return id_comprobante;
	}
	public void setId_comprobante(Integer id_comprobante) {
		this.id_comprobante = id_comprobante;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public Double getTotal_Gasto() {
		return total_Gasto;
	}
	public void setTotal_Gasto(Double total_Gasto) {
		this.total_Gasto = total_Gasto;
	}
	
	
}
